         <div class="membership-item">
            <h3>Probationary Members</h3>
            <ul>
                <li><strong>I.</strong> A mandatory step for all those desirous to be a part of the Society. This is an evaluation phase wherein desirous Member Application will be evaluated by the BOD.</li>
                <li><strong>II.</strong> Probationary Membership is Temporary and may last at most for a year. Desirous candidate may reapply after One Year to be reinstated as a Probationary member and be reconsidered if so desired and the BOD validates his request.</li>
                <li><strong>III.</strong> The Board shall review the performance of Probationary Members and decide upon the continuity of all the Probationary Membership at the interval of every 3 months.</li>
                <li><strong>IV.</strong> As per the evaluation, the Board may either award General Membership to the deserving ones who perform well & become eligible as per Byelaws or disallow the non-deserving ones & terminate their Probationary Membership or Extend/Continue the Probationary Membership till the next review.</li>
                <li><strong>V.</strong> Probationary Members will not be entitled to subscribe to the share capital of the society and to have any interest in the management of the society including the right to vote, contest election as a director of the board, or participate in the general body meetings of the society. However, they may be invited by the Board to witness the General Body Meets or any other meetings.</li>
                <li><strong>VI.</strong> Probationary Member shall pay the admission fee in full. They will not be issued any Shares in the Cooperative unless the duration of their Probationary Membership as allowed by the Board is completed. The Fee as paid thus far shall be non-refundable.</li>
            </ul>
        </div>

        <!-- General Members Section -->
        <div class="membership-item">
            <h3>General Members Criteria</h3>
            <ul>
                <li><strong>I.</strong> Is Competent to Contract under section 11 of the Indian Contract Act-1872. And an adult of above 18 years of age.</li>
                <li><strong>II.</strong> Is a Service Provider / Business Leader/ Professional including but not limited to being a: Expert or Excellence Practitioner or Researcher or Consultant or Management Proficient etc.</li>
                <li><strong>III.</strong> Resides within the Area of Operation as Registered with the Registrar of the Society.</li>
                <li><strong>IV.</strong> Agrees to the Organizational Vision & Values of the Society and is capable & willing to genuinely contribute towards fulfilment of the vision and objectives of the society in any manner whatsoever.</li>
                <li><strong>V.</strong> Is not a member of any other similar cooperative society of similar nature. Whose interest does not conflict with the interest of the Society.</li>
                <li><strong>VI.</strong> Has paid Admission Fee; Procured Minimum Shares of full value and Subscribed to the Quarterly Shares as prescribed in the Bye-laws.</li>
            </ul>
        </div>

        <!-- Associate Member Section -->
        <div class="membership-item">
            <h3>Associate Member</h3>
            <ul>
                <li><strong>I.</strong> Admission as an Associate Member will be basis of a ‘non-refundable fee’ as prescribed from time to time by the Executive Committee/ BOD.</li>
                <li><strong>II.</strong> The duration of membership validity is limited to ‘maximum one year only’ and membership can be renewed annually on payment of the prescribed fee after approval of the Executive Committee/BOD.</li>
                <li><strong>III.</strong> In the interest of the Society, Associate Members may even be issued ‘non-voting shares’ on such financial terms & conditions as agreed upon mutually but shall not confer any interest in the management of the multi-State co-operative society including right to vote. The duration of validity of membership in this case shall be limited to such period as decided by the Executive Committee/BOD. There will be no concession on Point II as noted above.</li>
                <li><strong>IV.</strong> Students, CSR Organisations, Business Leaders, and retired experts/ civil servants may also be encouraged to take membership as an associate member. This will help to introduce the cooperative culture and SARATHI’s vision to the new generations and also gather wide support for Society’s effortss.</li>
                <li><strong>V.</strong> In any case whatsoever, none of the Associate Members shall be allowed to have any interest or interference in the management of the society including right to vote, contest election as director of the board or participate in the general body meetings of the society.</li>
            </ul>
        </div>

        <!-- Employees as Members Section -->
        <div class="membership-item">
            <h3>Employees as Members</h3>
            <ul>
                <li><strong>I.</strong> The regular employees of SARATHI may be enrolled as shareholder only after 2 continuous years of satisfactory service in the cooperative but shall not be entitled to contest or vote at the election of the delegates and the Board of Society.</li>
                <li><strong>II.</strong> An existing regular employ of SARATHI (who also happens to be the shareholder) on retirement from service would cease to the member of the society. The Society will cancel his membership/Share certificate in such cases and shall refund the share capital held by him in the Society.</li>
            </ul>
